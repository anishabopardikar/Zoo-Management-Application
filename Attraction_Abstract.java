package org.example;

public abstract class Attraction_Abstract {
    public abstract String getStatus();
    public abstract void setStatus(String status);
    public abstract String getName();
    public abstract void setName(String name);
    public abstract double getTicketPrice();
    public abstract void setTicketPrice(double ticketPrice);
    public abstract String getDescription();
    public abstract void setDescription(String description);
    public abstract int getI();
    public abstract void setI(int count);
    public abstract void setAttractionId(int attractionId);

}
